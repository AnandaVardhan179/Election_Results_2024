import matplotlib.pyplot as plt
import numpy as np

parties = ['TDP', 'JSP', 'BJP', 'YSRCP']
seats_2019 = [23, 1, 0, 151]
seats_2024 = [135, 21, 8, 11]


bar_width = 0.35
index = np.arange(len(parties))


fig, ax = plt.subplots(figsize=(10, 6))

bar1 = ax.bar(index, seats_2019, bar_width, label='2019')
bar2 = ax.bar(index + bar_width, seats_2024, bar_width, label='2024')


ax.set_xlabel('Parties')
ax.set_ylabel('Number of Seats')
ax.set_title('Seats Won by Parties in Andhra Pradesh (2019 vs 2024)')
ax.set_xticks(index + bar_width / 2)
ax.set_xticklabels(parties)
ax.legend()


for rect in bar1:
    height = rect.get_height()
    ax.annotate(f'{height}',
                xy=(rect.get_x() + rect.get_width() / 2, height),
                xytext=(0, 3),
                textcoords='offset points',
                ha='center', va='bottom')

for rect in bar2:
    height = rect.get_height()
    ax.annotate(f'{height}',
                xy=(rect.get_x() + rect.get_width() / 2, height),
                xytext=(0, 3),
                textcoords='offset points',
                ha='center', va='bottom')

plt.tight_layout()
plt.show()